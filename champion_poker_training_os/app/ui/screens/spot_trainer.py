    def _refresh_library(self) -> None:
        _clear(self._cards_layout)

        cat = self._cat_combo.currentText() if hasattr(self, "_cat_combo") else "All"
        src = self._src_combo.currentText() if hasattr(self, "_src_combo") else "All"

        if "Hatalar" in src:
            # GERÇEK oynanmış hata spotları (board/kart/pozisyon ile yeniden oyna)
            try:
                from app.db.repository import get_mistake_spots
                drills = get_mistake_spots()
            except Exception:
                drills = []
            if not drills:
                self._cards_layout.addWidget(_mono(
                    "Henüz kayıtlı hata spotun yok. Real Experience modda el oyna; "
                    "GTO'dan saptığın gerçek eller buraya düşecek."))
                return
        else:
            drills = self.lib.get_drills(cat)
            if src == "Seeded":
                drills = [d for d in drills if d.get("source") == "seeded"]
            elif src == "From Leaks":
                drills = [d for d in drills if d.get("source") == "leak"]
            if not drills:
                self._cards_layout.addWidget(_mono("Henüz bu kategoride drill yok."))
                return

        for drill in drills:
            score = self.lib.get_score(drill["id"])
            card = _DrillCard(drill, score)
            card.play_clicked.connect(self._play_single)
            self._cards_layout.addWidget(card)

    def _on_drill_added(self, _drill: dict) -> None:
        """Called when LeakFinder adds a new drill — refresh library if visible."""
        if hasattr(self, "_cards_layout") and self._cards_layout.parent():
            self._refresh_library()

    # ── Queue logic ────────────────────────────────────────────────────

    def _start_all_queue(self) -> None:
        cat = self._cat_combo.currentText() if hasattr(self, "_cat_combo") else "All"
        self._queue = self.lib.get_drills(cat)[:30]   # cap at 30
        self._q_idx = 0
        self._play_next()

    def _play_single(self, drill: dict) -> None:
        self._queue = [drill]
        self._q_idx = 0
        self._play_next()

    def _play_next(self) -> None:
        if self._q_idx >= len(self._queue):
            self._on_queue_done()
            return
        self._current_drill = self._queue[self._q_idx]
        self._q_idx += 1
        self._update_score_bar()
        self._show_play()

    def _on_queue_done(self) -> None:
        self.coach_message.emit(
            f"Queue tamamlandı! {self._q_idx} drill oynadın. "
            "Drill Library'ye dönüyorsun — sonuçlar kaydedildi."
        )
        self._show_library()

    def _update_score_bar(self) -> None:
        total = len(self._queue)
        idx = self._q_idx
        self._score_queue_lbl.setText(
            f"DRILL  {idx} / {total}  ·  {self._current_drill['id'] if self._current_drill else '—'}"
        )

