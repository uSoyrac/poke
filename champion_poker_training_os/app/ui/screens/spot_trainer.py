    # ── Game loop ──────────────────────────────────────────────────────

    def _deal_next(self) -> None:
        if not self._game:
            return
        self._next_drill_btn.hide()
        self._game.start_hand()
        self._refresh()
        hand = self._game.current_hand
        if hand and not self._game.is_waiting_for_hero and not hand.is_complete:
            self._bot_timer.start()

    def _tick_bot(self) -> None:
        if not self._game or not self._game.current_hand:
            self._bot_timer.stop()
            return
        keep = self._game.step_action()
        self._refresh()
        if not keep:
            self._bot_timer.stop()
            if self._game.current_hand and self._game.current_hand.is_complete:
                self._on_hand_complete()

    def _hero_action(self, action_type: ActionType) -> None:
        if not self._game or not self._game.is_waiting_for_hero:
            return
        hand = self._game.current_hand
        hero = hand.hero
        amount = 0.0
        if action_type in (ActionType.BET, ActionType.RAISE):
            amount = self._size_bb()
        elif action_type == ActionType.CALL:
            amount = hand.to_call(hand.hero_idx)
        elif action_type == ActionType.ALL_IN:
            amount = hero.stack if hero else 0.0

        action_str = _action_str(action_type, hand, amount)
        self._game.hero_act(action_type, amount)
        self._refresh()

        if hand.is_complete:
            self._show_drill_feedback(action_str)
            self._on_hand_complete()
        else:
            self._bot_timer.start()

    def _on_hand_complete(self) -> None:
        self._next_drill_btn.show()
        self._update_buttons_visible()

    def _stop_game(self) -> None:
        if hasattr(self, "_bot_timer"):
            self._bot_timer.stop()
        for sc in getattr(self, "_shortcuts", []):
            sc.setEnabled(False)
            sc.deleteLater()
        self._shortcuts = []
        self._game = None

    # ── Refresh / render ───────────────────────────────────────────────

    def _refresh(self) -> None:
        if not self._game or not self._game.current_hand:
            return
        hand = self._game.current_hand
        hero = hand.hero

        action_top = self._game._action_queue[0] if self._game._action_queue else -1
        seats, hero_slot, dealer_slot = seats_from_hand(
            hand.players, hand.hero_idx,
            action_queue_top=action_top, unit="bb", hand=hand,
        )
        hero_cards = [c.display for c in hero.hole_cards] if (hero and hero.hole_cards) else None
        board = [c.display for c in hand.community]
        note = f"BLINDS {hand.small_blind:g}/{hand.big_blind:g}"

        hero_to_call = hand.to_call(hand.hero_idx) if hero else 0.0
        self.table.render_state(
            seats=seats,
            hero_slot_idx=hero_slot,
            dealer_slot_idx=dealer_slot,
            street=hand.street_name,
            board=board,
            pot=hand.pot,
            hero_cards=hero_cards,
            note=note,
            big_pot=(hand.street == Street.PREFLOP),
            show_opponent_backs=not hand.is_complete,
            to_call=hero_to_call,
        )

        if self._game.is_waiting_for_hero and hero_to_call > 0 and hand.pot > 0:
            pct = int(round(100 * hero_to_call / hand.pot))
            self._to_call_banner.setText(f"TO CALL  {hero_to_call:.1f} bb  ·  {pct}% POT")
            self._to_call_banner.show()
        else:
            self._to_call_banner.hide()

        self._update_buttons_visible()
        self._refresh_size()

    def _update_buttons_visible(self) -> None:
        for b in (self.fold_btn, self.check_btn, self.call_btn, self.raise_btn, self.allin_btn):
            b.hide()
        if not self._game or not self._game.is_waiting_for_hero:
            return
        hand = self._game.current_hand
        if not hand or hand.is_complete:
            return
        hero_idx = hand.hero_idx
        hero = hand.hero
        valid_types = {v[0] for v in hand.get_valid_actions(hero_idx)}
        to_call = hand.to_call(hero_idx)
        stk_ok = hero and hero.stack >= 0.05

        if ActionType.FOLD  in valid_types: self.fold_btn.show()
        if ActionType.CHECK in valid_types: self.check_btn.show()
        if ActionType.CALL  in valid_types:
            self.call_btn.setText(f"CALL  {to_call:.1f} bb")
            self.call_btn.show()
        elif to_call > 0 and ActionType.ALL_IN in valid_types and stk_ok:
            self.call_btn.setText(f"CALL ALL-IN  {hero.stack:.1f}")
            self.call_btn.show()
        if ActionType.BET   in valid_types:
            self.raise_btn.setText("BET"); self.raise_btn.show()
        if ActionType.RAISE in valid_types:
            self.raise_btn.setText("RAISE"); self.raise_btn.show()
        if stk_ok and to_call < hero.stack:
            self.allin_btn.setText(f"ALL-IN  {hero.stack:.1f}")
            self.allin_btn.show()

    def _size_bb(self) -> float:
        hand = self._game.current_hand
        hero = hand.hero
        pct = self.size_slider.value() / 100.0
        if hand.street == Street.PREFLOP and hand.pot <= hand.big_blind * 3:
            target = hand.big_blind * (2.0 + pct * 1.5)
        else:
            target = hand.pot * pct
        floor = max(hand.last_full_raise_size, hand.big_blind)
        if hand.current_bet > 0:
            target = max(target, hand.current_bet + floor - hero.current_bet)
        else:
            target = max(target, hand.big_blind)
        return round(min(target, hero.stack), 2)

    def _refresh_size(self) -> None:
        if not self._game or not self._game.current_hand:
            return
        chips = self._size_bb()
        pot = max(self._game.current_hand.pot, 0.01)
        pct = int(round(100 * chips / pot))
        self.size_label.setText(f"{chips:.1f} bb  ·  {pct}% pot")

    # ── Solver preview & feedback ──────────────────────────────────────

    def _show_solver_preview(self) -> None:
        _clear(self._feedback_layout)
        drill = self._current_drill
        if drill is None:
            return
        result = solve_spot(drill)
        hdr = QLabel("SOLVER BASELINE")
        hdr.setObjectName("TLabel")
        hdr.setStyleSheet("font-size:10px; letter-spacing:1px;")
        self._feedback_layout.addWidget(hdr)

        grid = QGridLayout()
        grid.setSpacing(4)
        for idx, action in enumerate(result.actions):
            bar = SolverFrequencyBar(action.action, action.frequency, action.ev, action.sizing)
            grid.addWidget(bar, idx // 2, idx % 2)
        self._feedback_layout.addLayout(grid)

        conf = QLabel(f"Source: {result.source_confidence}")
        col = _AMBER if "Mock" in result.source_confidence else _GREEN
        conf.setStyleSheet(f"font-family:'JetBrains Mono',monospace; font-size:9px; color:{col};")
        self._feedback_layout.addWidget(conf)

    def _show_drill_feedback(self, hero_action_str: str) -> None:
        _clear(self._feedback_layout)
        drill = self._current_drill
        if drill is None:
            return

        result = compare_action(drill, hero_action_str)
        score = score_decision(result["ev_loss"], result["solver_frequency"])
        self.lib.record_score(drill["id"], score)
        self.state.record_decision(
            result["is_correct"], result["ev_loss"],
            f"{drill['id']} {hero_action_str}: -{result['ev_loss']:.2f}bb"
        )

        # Score chip
        col = _GREEN if result["is_correct"] else _RED
        verdict_lbl = QLabel(
            f"{'✓' if result['is_correct'] else '✗'}  {hero_action_str.upper()}"
        )
        verdict_lbl.setStyleSheet(
            f"font-family:'JetBrains Mono',monospace; font-size:13px; "
            f"font-weight:700; color:{col};"
        )
        self._feedback_layout.addWidget(verdict_lbl)

        detail = QLabel(
            f"Best: {result['best_action']}  |  EV loss: {result['ev_loss']:+.2f}bb\n"
            f"Score: {score}/100  ({skill_label(score)})"
        )
        detail.setObjectName("Muted")
        detail.setWordWrap(True)
        detail.setStyleSheet("font-size:10px;")
        self._feedback_layout.addWidget(detail)
        self._feedback_layout.addWidget(_sep())

        # Mini solver grid
        hdr = QLabel("SOLVER FREQUENCIES")
        hdr.setObjectName("TLabel")
        hdr.setStyleSheet("font-size:10px;")
        self._feedback_layout.addWidget(hdr)
        grid = QGridLayout()
        grid.setSpacing(4)
        for idx, action in enumerate(result["solver"]["actions"]):
            bar = SolverFrequencyBar(
                action["action"], action["frequency"],
                action["ev"], action.get("sizing", "")
            )
            grid.addWidget(bar, idx // 2, idx % 2)
        self._feedback_layout.addLayout(grid)

        # Update score bar
        self._score_total_lbl.setText(f"Last score: {score}/100  {skill_label(score)}")
        self.coach_message.emit(explain_spot(drill, hero_action_str))

