# ── helpers ─────────────────────────────────────────────────────────────

def _label(text: str, obj: str = "TLabel") -> QLabel:
    l = QLabel(text)
    l.setObjectName(obj)
    return l


def _mono(text: str = "", size: int = 11) -> QLabel:
    l = QLabel(text)
    l.setStyleSheet(
        f"font-family:'JetBrains Mono',monospace; font-size:{size}px; color:#898d80;"
    )
    return l


def _sep() -> QFrame:
    f = QFrame()
    f.setFrameShape(QFrame.HLine)
    f.setStyleSheet(f"background:{_BORDER}; border:none; max-height:1px;")
    return f


def _clear(layout) -> None:
    while layout.count():
        item = layout.takeAt(0)
        w = item.widget()
        child = item.layout()
        if w:
            w.deleteLater()
        if child:
            _clear(child)


def _table_size(table_str: str) -> int:
    """Return number of players (including hero) from a table string."""
    if "HU" in table_str or "Heads" in table_str:
        return 2
    if "9" in table_str:
        return 9
    return 6   # default 6-max


def _action_str(action_type: ActionType, hand, size_bb: float) -> str:
    """Map engine ActionType to a drill option string."""
    if action_type == ActionType.FOLD:
        return "fold"
    if action_type == ActionType.CHECK:
        return "check"
    if action_type == ActionType.CALL:
        return "call"
    if action_type == ActionType.ALL_IN:
        return "jam"
    # RAISE / BET — classify by sizing relative to pot
    pot = max(hand.pot, 0.01)
    ratio = size_bb / pot
    if ratio <= 0.40:
        return "bet small"
    if ratio <= 0.70:
        return "bet medium"
    if ratio <= 1.10:
        return "bet large"
    return "raise"


